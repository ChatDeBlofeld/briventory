package models;

/** The {@code RGBA} class represents the Red / Green / Blue / Alpha format of the color of an image. */
public class RGBA {

  /** The value of the Red component. */
  private short red;
  /** The value of the Green component. */
  private short green;
  /** The value of the Blue component. */
  private short blue;
  /** The value of the transparency. */
  private float alpha;

}
